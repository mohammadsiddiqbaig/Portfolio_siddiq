import "./styles/Work.css";
import WorkImage from "./WorkImage";
import gsap from "gsap";
import { ScrollTrigger } from "gsap/ScrollTrigger";
import { useGSAP } from "@gsap/react";

gsap.registerPlugin(useGSAP);

const projects = [
  {
    title: "E-Commerce Web Application",
    category: "Full Stack / E-Commerce",
    tools: "MongoDB, Express.js, React.js, Node.js, Redux, JWT, bcrypt, Tailwind CSS, Sandbox Payment Gateway",
    image: "/images/project_ecommerce.png",
  },
  {
    title: "Online Course Management System",
    category: "Full Stack / EdTech",
    tools: "React.js, Node.js, Express.js, MongoDB, Mongoose, JWT, Tailwind CSS, Video/Document Uploads, Node.js Cron Email Jobs",
    image: "/images/project_lms.png",
  },
  {
    title: "Techyfy Frontend Applications",
    category: "Frontend / React Applications",
    tools: "HTML5, CSS3, JavaScript, React.js, Custom Hooks, Redux, Tailwind CSS, REST APIs (Axios/Fetch), Jest, React Testing Library",
    image: "/images/project_techyfy.png",
  },
];

const Work = () => {
  useGSAP(() => {
  let translateX: number = 0;

  function setTranslateX() {
    const box = document.getElementsByClassName("work-box");
    const rectLeft = document
      .querySelector(".work-container")!
      .getBoundingClientRect().left;
    const rect = box[0].getBoundingClientRect();
    const parentWidth = box[0].parentElement!.getBoundingClientRect().width;
    let padding: number =
      parseInt(window.getComputedStyle(box[0]).padding) / 2;
    translateX = rect.width * box.length - (rectLeft + parentWidth) + padding;
  }

  setTranslateX();

  let timeline = gsap.timeline({
    scrollTrigger: {
      trigger: ".work-section",
      start: "top top",
      end: `+=${translateX}`, // Use actual scroll width
      scrub: true,
      pin: true,
      id: "work",
    },
  });

  timeline.to(".work-flex", {
    x: -translateX,
    ease: "none",
  });

  // Clean up (optional, good practice)
  return () => {
    timeline.kill();
    ScrollTrigger.getById("work")?.kill();
  };
}, []);
  return (
    <div className="work-section" id="work">
      <div className="work-container section-container">
        <h2>
          My <span>Work</span>
        </h2>
        <div className="work-flex">
          {projects.map((project, index) => (
            <div className="work-box" key={index}>
              <div className="work-info">
                <div className="work-title">
                  <h3>0{index + 1}</h3>

                  <div>
                    <h4>{project.title}</h4>
                    <p>{project.category}</p>
                  </div>
                </div>
                <h4>Tools and features</h4>
                <p>{project.tools}</p>
              </div>
              <WorkImage image={project.image} alt={project.title} />
            </div>
          ))}
        </div>
      </div>
    </div>
  );
};

export default Work;
